        <h3 id="overview">III. Overview</h3>
        <p>
            Computer science (CS) can often seem big, scary, and mysterious to non-computer science majors or those with 
            no CS experience. "How does my computer even work?" "What is going on in this box?" "What is 'coding' and why
            do all of my CS friends complain about it?" "Is there a full proof way of solving problems?" "Is there a program 
            that could do this for me?" "I'm interested in artificial intelligence but... what is it?" If you've ever asked 
            any of these questions, then this is the course for you. This course is <b>not</b> going to 
            go into extreme detail about anything and we're not going to be doing any extreme coding (I may have you do a
            little tiny exercise, but you'll be fine!). When you come out of this course you're going to have a solid 
            understanding of computer science, and you'll hopefully have an idea of how it will be applied in your future
            line of work and in your life as a whole. 
            <br> <br>
            This course will have 5 main sections: 
        </p>
        <ol>
            <li>
                The course will begin with an introduction which motivates the need for computer science, explains 
                why anyone should care about it and its relevance to society, discusses the history of computer science 
                and its speculated future directions, and gives an overview of the computer science curriculum offered 
                at UVa. 
            </li> 
            <br>
            <li> 
                This section is titled "From the Bottom Up" because it begins by teaching about theoretical computing 
                and then moves to the details of the inner workings of computer hardware, and then continually moves up 
                the levels of abstraction until it reaches the software level, which is the most advanced and the closest 
                to how a normal user (like you!) views computers.
            </li>
            <br>
            <li>
                Computing gives us a way to solve problems and solve them quickly. What is our method for doing this, 
                you might ask? Algorithms. This section will start with an overview of computational problem solving 
                and will then teach about specific algorithms and problem solving strategies. It will also discuss 
                how we can evaluate an algorithm and what makes an algorithm "good" for the problem we are trying to solve.
                It will wrap up with a discussion of problems that we have not been able to effectively solve. <br>
            </li>
            <br>
            <li>
                There are so many interesting and popular topics in CS that we could learn about, so this section of the 
                class will cover a few of those. The first topic covered will be articial intelligence, what it is, the low 
                level picture of how it works, what algorithms and CS methods it uses, and how it is used in the world. We
                will also talk about web design, databases, and security.
            </li>
            <br>
            <li>
                The last 3 weeks of the course entail a discussion of how CS is involved in other fields such as business,
                law, finance, manufacturing, entertainment, media, healthcare, environmental science, biology, and more. There
                will also be a discussion of the different careers that a CS major could enter into, in case this course has 
                made you love CS so much that you want to move forward. <br>
            </li>    
        </ol>
                
        <h3 id="prereqs">IV. Background and Prerequisites</h3>
        <p>Absolutely no background in computer science is needed to take and succeed in this course, and there are no prerequisites.
            This course was designed specifically for those who have no experience with anything having to do with 
            computer science or computing in general. 
        </p>

        <br>
        
