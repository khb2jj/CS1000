        <h3 id="assignments">VI. Assignments</h3>
        <p>Approximately 8 take-home assignments will be used to reinforce topics learned in class.
            Each homework assignment will be worth approximately 7% of the course grade (may be adjusted 
            if assignments are added/dropped). These assignments will take you a little bit beyond what 
            is learned in class, requiring you to do the readings and some outside research in order to 
            complete them fully. As mentioned in the course overview, there will be no coding or any other 
            technical skill required to complete the homework assignments, only a strong understanding 
            of the topics discussed in class. 
        </p>

        <br>
        
        <h3 id="exams">VII. Exams</h3>
        <p> There will be three exams in this course, all held during the normal class periods. </p>
        <ol>
            <li>Exam 1: Friday, September 24th</li>
            <li>Exam 2: Friday, November 12th</li>
            <li>Final exam: Monday, December 6th</li>
        </ol>
        <p> Exams will be completed individually without collaboration, and will consist
            of a mix between short answer and multiple choice questions. The first exam will be 
            on all material covered before the exam date, the second exam will be on all material 
            between the first and second exams, and the final exam will be cumulative with emphasis 
            on the material covered between the second exam and the final exam. The first two exams
            will be worth 12% of the grade each, and the final exam will be worth 20% of your 
            overall grade. <br><br>
            A study guide will be provided by the instructor and TA's before each exam.
        </p>
        
